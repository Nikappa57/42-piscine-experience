int	ft_is_prime(int nb)
{
	int	i;
	int	j;

	i = 2;
	j = 2;
	if (nb < 2)
		return (0);
	while (i <= nb / 2)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
